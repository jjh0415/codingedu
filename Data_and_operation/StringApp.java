
public class StringApp {
    public static void main(String[] args) {
        System.out.println("Hello World"); // "" => String = Character들의 집합
        System.out.println('H'); // '' => Character = 한글자를 표현하는 데이터 타입
        System.out.println("H"); // String으로 인식

        System.out.println("Hello" + "World!"); // Str + Str
        System.out.println("Hello \nWorld"); // 줄바꿈
        System.out.println("Hello \tWorld"); // 탭
        System.out.println("Hello \"World\""); // Hello "World" => escape

    }
}
